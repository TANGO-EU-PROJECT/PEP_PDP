package com.example.demo.models;

public class Issuer {
	String id;
	String name;
	
	public Issuer() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}

